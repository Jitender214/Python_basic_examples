#logical operators and,or and not

print(1<2 and 2>3)
print(1<2 and 4>2)
print(('hello' =='hello') and 2==2) #using and both the conditions needs to be true

print('abc'=='abc' or '2>5') #or needs one of these conditions needs to be true
print(2==2 or 3==3)
print(4==3 or 4==5)

print(not('hello' == 'hellow'))
print(not('hello' == 'hello'))

print(100>200)
print(not 100>200)

